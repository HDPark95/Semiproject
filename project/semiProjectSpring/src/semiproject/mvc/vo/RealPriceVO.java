package semiproject.mvc.vo;

public class RealPriceVO {
	private int num, year;
	private String gu, dong, spc2, spc1, prc, flr;
	public int getNum() {
		return num;
	}
	public void setNum(int num) {
		this.num = num;
	}
	public int getYear() {
		return year;
	}
	public void setYear(int year) {
		this.year = year;
	}
	public String getGu() {
		return gu;
	}
	public void setGu(String gu) {
		this.gu = gu;
	}
	public String getDong() {
		return dong;
	}
	public void setDong(String dong) {
		this.dong = dong;
	}
	public String getSpc2() {
		return spc2;
	}
	public void setSpc2(String spc2) {
		this.spc2 = spc2;
	}
	public String getSpc1() {
		return spc1;
	}
	public void setSpc1(String spc1) {
		this.spc1 = spc1;
	}
	public String getPrc() {
		return prc;
	}
	public void setPrc(String prc) {
		this.prc = prc;
	}
	public String getFlr() {
		return flr;
	}
	public void setFlr(String flr) {
		this.flr = flr;
	}
}
