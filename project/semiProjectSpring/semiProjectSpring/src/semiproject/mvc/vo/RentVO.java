public class RentVO {
	// A1 = 매매
	// B1 = 전세
	// B2 = 월세
	private int prnum,pdnum;
	private String rentv,dan;
	private String rpay;
	
	
	public int getPrnum() {
		return prnum;
	}
	public void setPrnum(int prnum) {
		this.prnum = prnum;
	}
	public int getPdnum() {
		return pdnum;
	}
	public void setPdnum(int pdnum) {
		this.pdnum = pdnum;
	}
	public String getRentv() {
		return rentv;
	}
	public void setRentv(String rentv) {
		this.rentv = rentv;
	}
	public String getDan() {
		return dan;
	}
	public void setDan(String dan) {
		this.dan = dan;
	}
	public String getRpay() {
		return rpay;
	}
	public void setRpay(String rpay) {
		this.rpay = rpay;
	}


	
	
	
}
 